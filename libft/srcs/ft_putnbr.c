#include "libft.h"

void	ft_putnbr(int old_n)
{
	char			d;
	unsigned int	n;

	if (old_n < 0)
	{
		old_n *= -1;
		write(1, "-", 1);
	}
	n = old_n;
	if (n >= 10)
		ft_putnbr(n / 10);
	d = (n % 10) + '0';
	write(1, &d, 1);
}
