#include "libft.h"

int		ft_atoi(const char *str)
{
	long int	n;
	int			i;
	int			is_neg;

	n = 0;
	i = 0;
	is_neg = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		i++;
		is_neg = 1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	if (is_neg)
		n *= -1;
	return (n);
}
